// Hyper-Personalization Input Explorer MicroSim
// CANVAS_HEIGHT: 560
// Chapter 16: Personalization and Adaptive Learning Paths
// Bloom Level: Analyze (L4) — differentiate which output field each input controls
//
// Four independent boolean toggles (native p5 checkboxes) each feed a
// rule-based function that selects the concept, content format, and pacing
// note shown on a simulated "Recommended Lesson Plan" card. This is an
// ILLUSTRATIVE simulation using small lookup tables, NOT a live ML model.
// The learner isolates each input's contribution by toggling it on and off.

// ---- Standard responsive canvas globals ----
let canvasWidth = 400;          // responsive width, reset in updateCanvasSize()
let drawHeight = 480;           // top drawing region (no controls here)
let controlHeight = 80;         // control region: 2 rows (4 checkboxes + 2 buttons)
let canvasHeight = drawHeight + controlHeight; // = 560
let margin = 25;
let sliderLeftMargin = 140;     // present for standard compliance (no sliders used)
let defaultTextSize = 16;

// ---- Controls ----
let masteryCheckbox, quizCheckbox, prefsCheckbox, timeCheckbox;
let randomizeButton, resetButton;

// ---- Toggle state (all default OFF per spec) ----
let toggles = { mastery: false, quiz: false, prefs: false, time: false };

// ---- Simulated learner profile (regenerated by "Randomize") ----
let profile;

// ---- Change-highlight animation state ----
// Tracks which card field last changed so we can flash it.
let highlightField = null;      // 'concept' | 'format' | 'pacing' | null
let highlightStart = 0;
const HIGHLIGHT_MS = 900;

// Lookup tables for the simulated recommendation ------------------------------

// Concepts ordered by prerequisite depth; the "ready-to-learn" rule (Ch. 3)
// picks the shallowest concept the learner has NOT yet mastered.
const CONCEPT_POOL = [
  'Graph Vocabulary',
  'Nodes and Edges',
  'Directed Edges',
  'Cycle Detection',
  'Topological Sort',
  'Concept Dependencies'
];

// Preferred formats a learner might declare.
const PREF_FORMATS = ['video', 'text', 'MicroSim'];

function setup() {
  updateCanvasSize(); // MUST be first line
  const canvas = createCanvas(canvasWidth, canvasHeight);
  canvas.parent(document.querySelector('main'));

  textSize(defaultTextSize);

  // Load one preset profile by default.
  profile = presetProfile();

  // --- Row 1: four independent toggle checkboxes (all default off) ---
  masteryCheckbox = createCheckbox(' Mastery Record', false);
  masteryCheckbox.parent(document.querySelector('main'));
  masteryCheckbox.changed(() => setToggle('mastery', masteryCheckbox.checked()));

  quizCheckbox = createCheckbox(' Quiz Performance', false);
  quizCheckbox.parent(document.querySelector('main'));
  quizCheckbox.changed(() => setToggle('quiz', quizCheckbox.checked()));

  prefsCheckbox = createCheckbox(' Stated Preferences', false);
  prefsCheckbox.parent(document.querySelector('main'));
  prefsCheckbox.changed(() => setToggle('prefs', prefsCheckbox.checked()));

  timeCheckbox = createCheckbox(' Time-of-Day Pattern', false);
  timeCheckbox.parent(document.querySelector('main'));
  timeCheckbox.changed(() => setToggle('time', timeCheckbox.checked()));

  // --- Row 2: two buttons ---
  randomizeButton = createButton('Randomize Simulated Learner Data');
  randomizeButton.parent(document.querySelector('main'));
  randomizeButton.mousePressed(randomizeProfile);

  resetButton = createButton('Reset All Toggles');
  resetButton.parent(document.querySelector('main'));
  resetButton.mousePressed(resetToggles);

  layoutControls();

  describe(
    'An interactive explorer with four toggle checkboxes: Mastery Record, ' +
    'Quiz Performance, Stated Preferences, and Time-of-Day Pattern. Turning ' +
    'each toggle on changes a simulated Recommended Lesson Plan card, which ' +
    'shows a concept, a content format, and a pacing note. With all toggles ' +
    'off the card shows a generic non-personalized recommendation. Combining ' +
    'toggles demonstrates hyper-personalization from several independent signals.',
    LABEL
  );
}

function draw() {
  updateCanvasSize();

  // Region backgrounds (REQUIRED order) --------------------------------------
  fill('aliceblue');
  stroke('silver');
  strokeWeight(1);
  rect(0, 0, canvasWidth, drawHeight);
  fill('white');
  stroke('silver');
  rect(0, drawHeight, canvasWidth, controlHeight);

  // Title (after background) --------------------------------------------------
  fill('black');
  noStroke();
  textAlign(CENTER, TOP);
  textSize(24);
  text('Hyper-Personalization Input Explorer', canvasWidth / 2, 10);

  // Compute the current recommendation from active toggles.
  const rec = computeRecommendation();

  // Decide layout: side-by-side when wide, stacked when narrow (<640).
  const stacked = canvasWidth < 640;
  const contentTop = 46;

  if (stacked) {
    const panelX = margin;
    const panelW = canvasWidth - 2 * margin;
    drawInputPanel(panelX, contentTop, panelW, 150);
    drawLessonCard(panelX, contentTop + 162, panelW, 210, rec);
  } else {
    const leftW = min(320, canvasWidth * 0.42);
    drawInputPanel(margin, contentTop, leftW - margin, drawHeight - contentTop - 70);
    const rightX = leftW + 10;
    drawLessonCard(rightX, contentTop, canvasWidth - rightX - margin, drawHeight - contentTop - 70, rec);
  }

  // Caption line at the bottom of the drawing region.
  drawCaption(rec);

  // Control labels (row 1 group header) --------------------------------------
  noStroke();
  fill('black');
  textAlign(LEFT, CENTER);
  textSize(defaultTextSize);
}

// ---- Recommendation engine (rule-based, illustrative only) -----------------
// Each toggle controls exactly ONE field so the learner can attribute changes.
function computeRecommendation() {
  // Defaults with NO personalization (generic placeholder).
  let concept = 'Introductory Overview';
  let format = 'text';
  let pacing = 'Standard 20-minute session';
  let conceptDriver = null, formatDriver = null, pacingDriver = null;

  // Mastery Record -> selects the concept (ready-to-learn rule from Ch. 3).
  if (toggles.mastery) {
    concept = profile.readyConcept;
    conceptDriver = 'Mastery Record';
  }

  // Stated Preferences -> selects the content format (declared preference).
  if (toggles.prefs) {
    format = profile.preferredFormat;
    formatDriver = 'Stated Preferences';
  }

  // Quiz Performance -> if recent accuracy is low, shift to scaffolded format.
  // Quiz overrides a plain preference only when accuracy is low, so scaffolding
  // wins when the learner is struggling.
  if (toggles.quiz && profile.quizAccuracy < 0.7) {
    format = 'text + MicroSim';
    formatDriver = 'Quiz Performance';
  }

  // Time-of-Day Pattern -> adjusts the pacing note during a low-focus window.
  if (toggles.time) {
    if (profile.lowFocusWindow) {
      pacing = 'Shorter 10-minute segment recommended';
    } else {
      pacing = 'Full 20-minute session — peak focus window';
    }
    pacingDriver = 'Time-of-Day Pattern';
  }

  const activeCount =
    (toggles.mastery ? 1 : 0) + (toggles.quiz ? 1 : 0) +
    (toggles.prefs ? 1 : 0) + (toggles.time ? 1 : 0);

  return { concept, format, pacing, conceptDriver, formatDriver, pacingDriver, activeCount };
}

// ---- Left panel: toggle-state readout + underlying simulated data ----------
function drawInputPanel(x, y, w, h) {
  noStroke();
  fill(255, 255, 255, 230);
  stroke('silver');
  rect(x, y, w, h, 10);

  noStroke();
  fill('black');
  textAlign(LEFT, TOP);
  textSize(15);
  text('Simulated Learner Data', x + 12, y + 10);

  textSize(13);
  const rows = [
    ['Mastery Record', toggles.mastery, 'Ready concept: ' + profile.readyConcept],
    ['Quiz Performance', toggles.quiz, 'Recent accuracy: ' + Math.round(profile.quizAccuracy * 100) + '%'],
    ['Stated Preferences', toggles.prefs, 'Prefers: ' + profile.preferredFormat + '-first'],
    ['Time-of-Day Pattern', toggles.time,
      profile.lowFocusWindow ? 'Now: low-focus window' : 'Now: peak-focus window']
  ];
  let ry = y + 36;
  const lineH = (h - 44) / rows.length;
  for (const [label, on, detail] of rows) {
    // Status dot: green when the signal is active, gray when off.
    noStroke();
    fill(on ? 'mediumseagreen' : 'lightgray');
    circle(x + 20, ry + 8, 12);
    fill('black');
    textAlign(LEFT, TOP);
    textStyle(BOLD);
    text(label, x + 34, ry);
    textStyle(NORMAL);
    fill(on ? 'black' : 'gray');
    text(detail, x + 34, ry + 16);
    ry += lineH;
  }
}

// ---- Right panel: the live Recommended Lesson Plan card --------------------
function drawLessonCard(x, y, w, h, rec) {
  // Card background.
  noStroke();
  stroke('cornflowerblue');
  strokeWeight(2);
  fill('white');
  rect(x, y, w, h, 12);

  noStroke();
  fill('midnightblue');
  textAlign(LEFT, TOP);
  textSize(16);
  textStyle(BOLD);
  text('Recommended Lesson Plan', x + 14, y + 12);
  textStyle(NORMAL);

  const now = millis();
  const fieldY = y + 44;
  const rowH = (h - 56) / 3;

  drawCardField(x + 14, fieldY, w - 28, rowH,
    'Concept', rec.concept, rec.conceptDriver, 'concept', now);
  drawCardField(x + 14, fieldY + rowH, w - 28, rowH,
    'Content Format', rec.format, rec.formatDriver, 'format', now);
  drawCardField(x + 14, fieldY + 2 * rowH, w - 28, rowH,
    'Pacing', rec.pacing, rec.pacingDriver, 'pacing', now);
}

function drawCardField(x, y, w, h, label, value, driver, fieldKey, now) {
  // Flash a soft highlight on the field that just changed.
  let flash = 0;
  if (highlightField === fieldKey) {
    const t = now - highlightStart;
    if (t < HIGHLIGHT_MS) flash = 1 - t / HIGHLIGHT_MS;
    else highlightField = null;
  }
  if (flash > 0) {
    noStroke();
    fill(255, 235, 59, 200 * flash); // gold flash
    rect(x - 4, y, w + 8, h - 4, 6);
  }

  noStroke();
  fill('gray');
  textAlign(LEFT, TOP);
  textSize(12);
  text(label.toUpperCase(), x, y + 2);

  fill('black');
  textSize(16);
  textStyle(BOLD);
  // Value may wrap; keep it inside the field width.
  text(value, x, y + 18, w);
  textStyle(NORMAL);

  // Driver tag: which input controlled this field.
  if (driver) {
    textSize(11);
    fill('seagreen');
    textAlign(RIGHT, TOP);
    text('driven by: ' + driver, x + w, y + 2);
  } else {
    textSize(11);
    fill('silver');
    textAlign(RIGHT, TOP);
    text('generic default', x + w, y + 2);
  }
}

// ---- Caption line (Data Visibility stages) --------------------------------
function drawCaption(rec) {
  let msg;
  if (rec.activeCount === 0) {
    msg = 'No personalization inputs active';
  } else if (rec.activeCount === 4) {
    msg = 'This is hyper-personalization: four independent signals combined into one recommendation';
  } else {
    // Name the input that drove the most recent change, if known.
    const drivers = [];
    if (rec.conceptDriver) drivers.push(rec.conceptDriver + ' set the concept');
    if (rec.formatDriver) drivers.push(rec.formatDriver + ' set the format');
    if (rec.pacingDriver) drivers.push(rec.pacingDriver + ' set the pacing');
    msg = drivers.join('; ');
  }

  noStroke();
  fill('black');
  textAlign(CENTER, BOTTOM);
  textSize(14);
  textStyle(ITALIC);
  text(msg, canvasWidth / 2, drawHeight - 34, canvasWidth - 2 * margin);
  textStyle(NORMAL);

  fill('gray');
  textSize(11);
  text('Illustrative rule-based simulation — not a live machine-learning model',
    canvasWidth / 2, drawHeight - 10);
}

// ---- Toggle / profile handlers --------------------------------------------
function setToggle(key, val) {
  toggles[key] = val;
  // Highlight the field this input controls so the change is visible.
  const fieldFor = { mastery: 'concept', quiz: 'format', prefs: 'format', time: 'pacing' };
  triggerHighlight(fieldFor[key]);
}

function triggerHighlight(field) {
  highlightField = field;
  highlightStart = millis();
}

function resetToggles() {
  toggles = { mastery: false, quiz: false, prefs: false, time: false };
  masteryCheckbox.checked(false);
  quizCheckbox.checked(false);
  prefsCheckbox.checked(false);
  timeCheckbox.checked(false);
  highlightField = null;
}

// A fixed preset so the default view is deterministic.
function presetProfile() {
  return {
    readyConcept: 'Cycle Detection',
    quizAccuracy: 0.58,        // low -> quiz toggle triggers scaffolding
    preferredFormat: 'video',
    lowFocusWindow: true
  };
}

// Regenerate a fresh random simulated learner profile.
function randomizeProfile() {
  profile = {
    readyConcept: random(CONCEPT_POOL),
    quizAccuracy: round(random(0.35, 0.95) * 100) / 100,
    preferredFormat: random(PREF_FORMATS),
    lowFocusWindow: random() < 0.5
  };
  // Flash all three fields to signal the underlying data changed.
  triggerHighlight('concept');
}

// ---- Control layout (responsive) ------------------------------------------
function layoutControls() {
  const row1Y = drawHeight + 6;
  const row2Y = drawHeight + 44;

  if (canvasWidth < 640) {
    // Two toggles per line to fit narrow widths.
    const colW = canvasWidth / 2;
    masteryCheckbox.position(10, row1Y);
    quizCheckbox.position(colW + 10, row1Y);
    prefsCheckbox.position(10, row1Y + 20);
    timeCheckbox.position(colW + 10, row1Y + 20);
    randomizeButton.position(10, row2Y + 8);
    resetButton.position(10, row2Y + 34);
  } else {
    // Four toggles across one row.
    const colW = (canvasWidth - 20) / 4;
    masteryCheckbox.position(10, row1Y);
    quizCheckbox.position(10 + colW, row1Y);
    prefsCheckbox.position(10 + 2 * colW, row1Y);
    timeCheckbox.position(10 + 3 * colW, row1Y);
    randomizeButton.position(10, row2Y);
    resetButton.position(280, row2Y);
  }
}

function windowResized() {
  updateCanvasSize();
  resizeCanvas(canvasWidth, canvasHeight);
  layoutControls();
  redraw();
}

function updateCanvasSize() {
  const container = document.querySelector('main').getBoundingClientRect();
  canvasWidth = Math.floor(container.width);
}
